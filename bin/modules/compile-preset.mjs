/**
 * Plan 05-03 (D-19, T-05-06 mitigation): essentials preset compile step.
 *
 * Compiles src/presets/essentials-v1.json -> src/presets/generated-index.ts.
 *
 * The compile step is the typo-resistance contract: every alias in the
 * human-editable JSON is checked against the fresh tool registry extracted
 * from src/generated/client.ts. An alias the registry does not know about
 * fails codegen loudly with a bounded preview of the offending names.
 *
 * Contract:
 *   - preset.version MUST equal "essentials-v1"
 *   - preset.ops MUST be an array of exactly 150 non-empty strings (D-19)
 *   - every op MUST appear as an `alias: "..."` literal in client.ts
 *   - output is deterministic: alias list sorted lexicographically so
 *     git diffs on preset evolution highlight real changes, not ordering drift
 *   - emitted ReadonlySet<string> is Object.freeze()d at module load time;
 *     consumers CANNOT mutate the preset set
 *
 * Invocation: chained from bin/generate-graph-client.mjs after generateMcpTools
 * and (under FULL_COVERAGE=1) runBetaPipeline. Running under FULL_COVERAGE=0
 * will throw on the 4 subscription ops (absent from the legacy 212-op
 * endpoints.json). That is acceptable per plan guidance — surfaces the
 * legacy/preset gap early rather than shipping a broken preset reference.
 */
import fs from 'fs';
import path from 'path';

/**
 * @param {string} generatedDir  Absolute path to src/generated (carries client.ts).
 * @param {string} presetsDir    Absolute path to src/presets (carries essentials-v1.json;
 *                                generated-index.ts is (over)written here).
 * @returns {{count: number, presetTsPath: string, missing: string[]}}
 * @throws {Error} when client.ts or essentials-v1.json is missing,
 *   or when the preset fails count/version/registry invariants.
 */
export function compileEssentialsPreset(generatedDir, presetsDir) {
  try {
    const clientPath = path.join(generatedDir, 'client.ts');
    const presetJsonPath = path.join(presetsDir, 'essentials-v1.json');
    const presetTsPath = path.join(presetsDir, 'generated-index.ts');

    if (!fs.existsSync(clientPath)) {
      throw new Error(`${clientPath} missing — run generate first`);
    }
    if (!fs.existsSync(presetJsonPath)) {
      throw new Error(`${presetJsonPath} missing`);
    }

    const clientCode = fs.readFileSync(clientPath, 'utf-8');
    // Registry extraction: every `alias: "..."` (and `alias: '...'`). Case-sensitive.
    // Matches the openapi-zod-client emission pattern and Plan 05-02's
    // `__beta__`-prefixed aliases uniformly.
    const registry = new Set(
      [...clientCode.matchAll(/alias:\s*["']([^"']+)["']/g)].map((m) => m[1])
    );

    const preset = JSON.parse(fs.readFileSync(presetJsonPath, 'utf-8'));
    if (preset.version !== 'essentials-v1') {
      throw new Error(`expected version "essentials-v1", got "${preset.version}"`);
    }
    if (!Array.isArray(preset.ops)) {
      throw new Error('preset.ops must be an array');
    }
    if (preset.ops.length !== 150) {
      throw new Error(`expected exactly 150 ops, got ${preset.ops.length}`);
    }
    // Defensive: reject empty/whitespace aliases before registry check so
    // the error preview below stays meaningful.
    for (const op of preset.ops) {
      if (typeof op !== 'string' || op.length === 0) {
        throw new Error(`preset.ops contains non-string or empty entry: ${JSON.stringify(op)}`);
      }
    }

    const missing = preset.ops.filter((op) => !registry.has(op));
    if (missing.length > 0) {
      const previewCount = Math.min(10, missing.length);
      const preview = missing.slice(0, previewCount).join(', ');
      const tail = missing.length > previewCount ? ` (and ${missing.length - previewCount} more)` : '';
      // T-05-06: a preset op that cannot be resolved against the fresh
      // registry is almost always a typo or a stale alias. Failing codegen
      // loudly is cheaper than shipping a preset whose `enabled_tools` set
      // silently drops half the flagship ops. The Phase 5 regeneration
      // (commit 82b8fc3) populated essentials-v1.json against the real
      // generated operationIds, so a real mismatch now means "upstream
      // renamed/removed an op we still reference" — operator must look.
      throw new Error(
        `${missing.length} preset op(s) NOT in registry: ${preview}${tail}`
      );
    }

    // Deterministic emit order: lexicographic sort on the alias list keeps
    // the generated file diff-friendly when the preset grows (e.g. v1 -> v2).
    const sortedOps = [...preset.ops].sort();
    const tsOut = [
      '// THIS FILE IS AUTO-GENERATED by bin/modules/compile-preset.mjs',
      '// DO NOT EDIT DIRECTLY. Regenerate with `npm run generate`.',
      '// Source: src/presets/essentials-v1.json',
      '',
      'export const ESSENTIALS_V1_OPS: ReadonlySet<string> = Object.freeze(new Set<string>([',
      ...sortedOps.map((op) => `  ${JSON.stringify(op)},`),
      ']));',
      '',
      'export const PRESET_VERSIONS: ReadonlyMap<string, ReadonlySet<string>> = new Map([',
      "  ['essentials-v1', ESSENTIALS_V1_OPS],",
      ']);',
      '',
    ].join('\n');

    fs.writeFileSync(presetTsPath, tsOut);
    return { count: sortedOps.length, presetTsPath, missing: [] };
  } catch (error) {
    throw new Error(`compile-preset: ${error.message}`);
  }
}
